import { obj, text } from "@/lib/content";
import type { MandatoParams } from "./mandato";

/*
  Traduzione AR di cortesia del mandato professionale.
  La versione italiana (mandato.ts) fa fede — vedi avviso finale e notice in pagina.
*/

export function buildMandatoParagraphsAr({
  practiceCode,
  signerName,
}: MandatoParams): string[] {
  const studio = obj<{
    ragione_sociale?: string;
    forma_giuridica?: string;
    piva?: string;
    cf?: string;
    albo?: string;
    indirizzo?: string;
  }>("footer", "studio", {});
  const pec = text("settings", "pec", "");
  const email = text("settings", "email", "");

  return [
    `أنا الموقّع/ة أدناه ${signerName} (فيما يلي، «العميل») أُفوّض ${
      studio.ragione_sociale ?? "Geom. Lorenzo Armellin"
    }، ${studio.indirizzo ?? ""}، الرقم الضريبي P.IVA ${studio.piva ?? ""}، الرقم الضريبي الشخصي C.F. ${
      studio.cf ?? ""
    }، المقيد في ${
      studio.albo ?? "النقابة المهنية"
    }، PEC ${pec}، البريد ${email} (فيما يلي، «المهني»)، بالمهمة المهنية المبيّنة أدناه، المتعلقة بالمعاملة ${practiceCode}.`,

    "١. الموضوع. تشمل المهمة المساعدة في إعداد تصريحة الميراث وإرسالها إلكترونيًا إلى وكالة الإيرادات (Agenzia delle Entrate)، التي يؤديها المهني بصفته وسيطًا مرخّصًا Entratel، وكذلك الإجراءات المتصلة المنصوص عليها في الحزمة المشتراة (مثل نقل الملكية الكادسترالية). تُستثنى الأنشطة غير المشمولة في الحزمة، ويمكن أن تكون موضوع عرض سعر منفصل.",

    "٢. المقابل. الأتعاب هي أتعاب الحزمة المشتراة، المبينة في قسم «مشترياتك» في المنطقة الشخصية والمدفوعة أصلًا عند الطلب. الضرائب والرسوم المستحقة للدولة (ضريبة الميراث، الضريبة العقارية، ضريبة السجل، الطوابع، الرسوم الخاصة) غير مشمولة في الأتعاب: تُحسب وفق الحالة المحددة، ويُبلَّغ بها العميل قبل إرسال التصريح، ويدفعها العميل دون أي هامش من المهني.",

    "٣. التزامات العميل. يلتزم العميل بتقديم بيانات ومستندات صحيحة وكاملة وفي الوقت المناسب، ويبقى مسؤولًا عن صحتها. مواعيد التسليم المشار إليها تبدأ من لحظة اكتمال الوثائق اللازمة واعتمادها من المهني، ولا تشمل أوقات الجهات الثالثة (وكالة الإيرادات، السجل العقاري، البنوك).",

    "٤. العناية والمسؤولية. ينفّذ المهني المهمة بالعناية المهنية التي تقتضيها طبيعة النشاط (التزام بوسائل)، مستعينًا بإشراف ضريبي من محاسب قانوني. فيما لم يُنص عليه في هذا التفويض تُطبَّق شروط البيع المقبولة عند الشراء، بما فيها أحكام حدود المسؤولية وتعديل الحزمة والتسوية.",

    "٥. العدول. يبقى حق العدول قائمًا وفق المادتين 52–59 من قانون المستهلك، على النحو المبيّن في شروط البيع وصفحة العدول في الموقع. في حال العدول بعد بدء المعالجة، يدفع العميل مبلغًا متناسبًا مع الخدمة المقدَّمة أصلًا.",

    "٦. البيانات الشخصية. معالجة البيانات الشخصية للعميل وللأشخاص المعنيين بالمعاملة (بما فيهم المتوفى والورثة الآخرون) موضحة في إشعار الخصوصية المتاح على الموقع، الذي يصرّح العميل بأنه اطّلع عليه. يضمن العميل أنه مخوّل بإبلاغ بيانات أطراف ثالثة.",

    "٧. منح التفويض والتوقيع. يُمنح هذا التفويض عن بُعد. يتم التوقيع، باختيار العميل، إلكترونيًا (تحديد خانة القبول وزر التوقيع في المنطقة الشخصية، مع تسجيل التاريخ والوقت) أو بالتوقيع بخط اليد على المستند الذي تم تنزيله ثم رفع النسخة الموقّعة.",

    "(النسخة الإيطالية من هذا التفويض هي المعتمدة قانونًا. أي ترجمة هي للتيسير فقط، وعند الاختلاف تسود النسخة الإيطالية.)",
  ];
}

export function buildMandatoTextAr(params: MandatoParams): string {
  return (
    `تفويض مهني — المعاملة ${params.practiceCode}\n\n` +
    buildMandatoParagraphsAr(params).join("\n\n") +
    `\n\nالمكان والتاريخ: _________________________\n\nتوقيع العميل: _________________________\n`
  );
}
