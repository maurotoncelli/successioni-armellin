import { obj, text } from "@/lib/content";
import type { MandatoParams } from "./mandato";

/*
  Russian courtesy translation of the professional mandate.
  The Italian version (mandato.ts) is binding — see final notice and page notice.
*/

export function buildMandatoParagraphsRu({
  practiceCode,
  signerName,
}: MandatoParams): string[] {
  const studio = obj<{
    ragione_sociale?: string;
    forma_giuridica?: string;
    piva?: string;
    cf?: string;
    albo?: string;
    indirizzo?: string;
  }>("footer", "studio", {});
  const pec = text("settings", "pec", "");
  const email = text("settings", "email", "");

  return [
    `Я, нижеподписавшийся(аяся) ${signerName} (далее — «Клиент»), поручаю ${
      studio.ragione_sociale ?? "Geom. Lorenzo Armellin"
    }, ${studio.indirizzo ?? ""}, P.IVA ${studio.piva ?? ""}, C.F. ${
      studio.cf ?? ""
    }, зарегистрированному в ${
      studio.albo ?? "профессиональном реестре"
    }, PEC ${pec}, e-mail ${email} (далее — «Профессионал»), профессиональное поручение, описанное ниже, по делу ${practiceCode}.`,

    "1. ПРЕДМЕТ. Поручение включает содействие в подготовке декларации о наследстве и её электронную подачу в Agenzia delle Entrate, которую Профессионал выполняет как уполномоченный посредник Entratel, а также связанные действия, предусмотренные приобретённым пакетом (напр. кадастровые переоформления / volture). Действия, не входящие в пакет, исключены и могут быть предметом отдельной сметы.",

    "2. ГОНОРАР. Гонорар соответствует приобретённому пакету, указанному в разделе «Ваша покупка» личного кабинета и уже оплаченному при заказе. Налоги и сборы, причитающиеся государству (налог на наследство, ипотечный и кадастровый налоги, марки, специальные сборы), НЕ входят в гонорар: они рассчитываются по конкретному случаю, сообщаются Клиенту до подачи декларации и уплачиваются Клиентом без какой-либо надбавки со стороны Профессионала.",

    "3. ОБЯЗАННОСТИ КЛИЕНТА. Клиент обязуется предоставить точные, полные и своевременные данные и документы и несёт ответственность за их достоверность. Сроки исполнения начинаются с момента, когда необходимая документация полна и проверена Профессионалом, и не включают сроки третьих лиц (Agenzia delle Entrate, кадастр, банки).",

    "4. ДОЛЖНАЯ ЗАБОТЛИВОСТЬ И ОТВЕТСТВЕННОСТЬ. Профессионал исполняет поручение с профессиональной заботливостью, требуемой характером деятельности (обязательство средств), с налоговым надзором присяжного налогового консультанта. Ко всему, не предусмотренному настоящим мандатом, применяются Условия продажи, принятые при покупке, включая ограничения ответственности, изменение пакета и расчёт.",

    "5. ОТКАЗ. Право отказа, предусмотренное ст. 52–59 Кодекса потребителей, сохраняется в том виде, как указано в Условиях продажи и на странице «Отказ» сайта. В случае отказа после начала работ Клиент оплачивает сумму, пропорциональную уже оказанной услуге.",

    "6. ПЕРСОНАЛЬНЫЕ ДАННЫЕ. Обработка персональных данных Клиента и лиц, затронутых делом (включая умершего и других наследников), описана в Информации о конфиденциальности на сайте, которую Клиент заявляет прочитанной. Клиент гарантирует, что уполномочен передавать данные третьих лиц.",

    "7. ПОРУЧЕНИЕ И ПОДПИСЬ. Настоящий мандат выдаётся дистанционно. Подпись осуществляется по выбору Клиента электронно (галочка принятия и кнопка подписи в личном кабинете с фиксацией даты и времени) или собственноручно на скачанном документе с последующей загрузкой подписанной копии.",

    "(Итальянская версия настоящего мандата имеет юридическую силу. Любой перевод предоставляется лишь для удобства; при расхождениях действует итальянская версия.)",
  ];
}

export function buildMandatoTextRu(params: MandatoParams): string {
  return (
    `Профессиональный мандат — Дело ${params.practiceCode}\n\n` +
    buildMandatoParagraphsRu(params).join("\n\n") +
    `\n\nМесто и дата: _________________________\n\nПодпись Клиента: _________________________\n`
  );
}
