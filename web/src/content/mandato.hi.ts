import { obj, text } from "@/lib/content";
import type { MandatoParams } from "./mandato";

/*
  Hindi courtesy translation of the professional mandate.
  The Italian version (mandato.ts) is binding — see final notice and page notice.
*/

export function buildMandatoParagraphsHi({
  practiceCode,
  signerName,
}: MandatoParams): string[] {
  const studio = obj<{
    ragione_sociale?: string;
    forma_giuridica?: string;
    piva?: string;
    cf?: string;
    albo?: string;
    indirizzo?: string;
  }>("footer", "studio", {});
  const pec = text("settings", "pec", "");
  const email = text("settings", "email", "");

  return [
    `मैं, अधोहस्ताक्षरी ${signerName} (इसके बाद, «ग्राहक»), ${
      studio.ragione_sociale ?? "Geom. Lorenzo Armellin"
    }, ${studio.indirizzo ?? ""}, P.IVA ${studio.piva ?? ""}, C.F. ${
      studio.cf ?? ""
    }, ${
      studio.albo ?? "पेशेवर रजिस्टर"
    } में पंजीकृत, PEC ${pec}, ईमेल ${email} (इसके बाद, «पेशेवर») को नीचे वर्णित पेशेवर कार्य सौंपता/सौंपती हूँ, जो फ़ाइल ${practiceCode} से संबंधित है।`,

    "1. विषय। कार्य में उत्तराधिकार घोषणा तैयार करने में सहायता और उसे Agenzia delle Entrate को इलेक्ट्रॉनिक रूप से भेजना शामिल है, जिसे पेशेवर अधिकृत Entratel मध्यस्थ के रूप में करता है, तथा खरीदे गए पैकेज में निर्धारित संबंधित कदम (जैसे कैडस्ट्रल ट्रांसफ़र / volture)। पैकेज में शामिल न गतिविधियाँ बाहर हैं और अलग अनुमान का विषय हो सकती हैं।",

    "2. शुल्क। शुल्क खरीदे गए पैकेज का है, व्यक्तिगत क्षेत्र के «आपकी खरीद» अनुभाग में दर्शाया गया और ऑर्डर पर पहले ही चुकाया गया। राज्य को देय कर और शुल्क (उत्तराधिकार कर, बंधक और कैडस्ट्रल कर, स्टैम्प, विशेष शुल्क) शुल्क में शामिल नहीं हैं: वे ठोस स्थिति के अनुसार गणना किए जाते हैं, घोषणा भेजने से पहले ग्राहक को बताए जाते हैं, और ग्राहक द्वारा चुकाए जाते हैं, पेशेवर की ओर से कोई अधिभार नहीं।",

    "3. ग्राहक के दायित्व। ग्राहक सटीक, पूर्ण और समय पर डेटा व दस्तावेज़ देने का वचन देता है और उनकी सटीकता के लिए ज़िम्मेदार रहता है। डिलीवरी समय सीमा तब से चलती है जब आवश्यक दस्तावेज़ पूरे हों और पेशेवर द्वारा सत्यापित हों, और इसमें तीसरे पक्ष (Agenzia delle Entrate, कैडस्ट्रे, बैंक) के समय शामिल नहीं हैं।",

    "4. सावधानी और दायित्व। पेशेवर गतिविधि की प्रकृति के अनुसार अपेक्षित पेशेवर सावधानी से कार्य करता है (साधनों का दायित्व), शपथबद्ध कर सलाहकार की कर निगरानी के साथ। इस अधिदेश में न बताई गई बातों के लिए खरीद पर स्वीकार की गई बिक्री शर्तें लागू होती हैं, जिनमें दायित्व की सीमाएँ, पैकेज परिवर्तन और निपटान शामिल हैं।",

    "5. वापसी। उपभोक्ता संहिता की धारा 52–59 में निर्धारित वापसी का अधिकार बिक्री शर्तों और साइट के वापसी पृष्ठ पर बताए अनुसार बना रहता है। कार्य शुरू होने के बाद वापसी पर, ग्राहक पहले से दी गई सेवा के अनुपात में राशि चुकाता है।",

    "6. व्यक्तिगत डेटा। ग्राहक और फ़ाइल से जुड़े व्यक्तियों (मृतक और अन्य उत्तराधिकारियों सहित) के व्यक्तिगत डेटा का प्रसंस्करण साइट पर उपलब्ध गोपनीयता सूचना में वर्णित है, जिसे ग्राहक ने पढ़ लिया है। ग्राहक गारंटी देता है कि वह तीसरे पक्ष के डेटा संप्रेषित करने के लिए अधिकृत है।",

    "7. प्रदान और हस्ताक्षर। यह अधिदेश दूरस्थ रूप से प्रदान किया जाता है। हस्ताक्षर ग्राहक की पसंद पर इलेक्ट्रॉनिक रूप से (व्यक्तिगत क्षेत्र में स्वीकृति बॉक्स और हस्ताक्षर बटन, तिथि व समय दर्ज) या डाउनलोड किए गए दस्तावेज़ पर हस्तलिखित हस्ताक्षर कर हस्ताक्षरित प्रति अपलोड करके होता है।",

    "(इस अधिदेश का इतालवी संस्करण कानूनी रूप से बाध्यकारी है। कोई भी अनुवाद केवल सुविधा के लिए है; विसंगति की स्थिति में इतालवी संस्करण मान्य होगा।)",
  ];
}

export function buildMandatoTextHi(params: MandatoParams): string {
  return (
    `पेशेवर अधिदेश — फ़ाइल ${params.practiceCode}\n\n` +
    buildMandatoParagraphsHi(params).join("\n\n") +
    `\n\nस्थान और तिथि: _________________________\n\nग्राहक के हस्ताक्षर: _________________________\n`
  );
}
